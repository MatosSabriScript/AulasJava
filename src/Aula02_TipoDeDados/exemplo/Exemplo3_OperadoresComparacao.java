package Aula02_TipoDeDados.exemplo;

public class Exemplo3_OperadoresComparacao {
    public static void main(String[] args) {
        int num1= 7, num2=7; //mudei o nÃºmero
        boolean resposta;

        resposta= num1>num2;
        System.out.println(num1+">"+num2+"="+resposta);

        resposta= num1<num2;
        System.out.println(num1+"<"+num2+"="+resposta);

        resposta= num1>=num2;
        System.out.println(num1+">="+num2+"="+resposta);

        resposta= num1<=num2;
        System.out.println(num1+"<="+num2+"="+resposta);

        resposta= num1!=num2;
        System.out.println(num1+"!="+num2+"="+resposta);





    }
}
